uint16_t adc_value;
uint16_t duty;
uint16_t get_adc_channel(uint32_t channel)
{
	uint16_t adc;
	//config channel
	ADC_ChannelConfTypeDef sConfig = {0};
	sConfig.Channel = channel;
	sConfig.Rank = 1;
	sConfig.SamplingTime = ADC_SAMPLETIME_144CYCLES;
	if (HAL_ADC_ConfigChannel(&hadc1, &sConfig) != HAL_OK)
	{
		Error_Handler();
	}

	//read adc
	HAL_ADC_Start(&hadc1);
	HAL_ADC_PollForConversion(&hadc1, 1000);
	adc = HAL_ADC_GetValue(&hadc1);
	HAL_ADC_Stop(&hadc1);
	return adc;
}

uint32_t map(uint32_t x, uint32_t in_min, uint32_t in_max, uint32_t out_min, uint32_t out_max){
  return (x - in_min) * (out_max - out_min) / (in_max - in_min) + out_min;
}

void set_motor(uint16_t adc_value)
{

    if (adc_value <= 40)
    {
        duty = 0;
    }
    else if (adc_value >= 4070)
    {
        duty = 100;
    }
    else
    {
        duty = map(adc_value, 40, 4070, 0, 100);
    }

    pwm_set_duty(&htim1, TIM_CHANNEL_1, duty);
}
///---------------------------------UART voi Jetson Nano-------------------------------------------------
char data_rx;
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(data_rx == '0')
	{
		HAL_GPIO_WritePin(GPIOC,GPIO_PIN_13,(GPIO_PinState) 1);
	}
	else if(data_rx == '1')
	{
		HAL_GPIO_WritePin(GPIOC,GPIO_PIN_13,(GPIO_PinState) 0);
	}
	
	HAL_UART_Receive_IT(&huart1,(uint8_t *)&data_rx,1);
}


